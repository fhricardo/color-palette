const familyContainer = document.querySelector(".family-container");

// loop through each family
palettes.forEach((family) => {
    const familyDiv = document.createElement("div");
    familyDiv.classList.add("family");

    console.log(familyDiv);

    const titleDiv = document.createElement("div");
    titleDiv.classList.add("title");
    titleDiv.textContent = family.family;

    console.log(titleDiv.textContent = family.family);

    familyDiv.appendChild(titleDiv);

    // loop through each palette in the family
    family.palettes.forEach((palette) => {
        const paletteDiv = document.createElement("div");
        paletteDiv.classList.add("palette");

        const paletteName = document.createElement("h4");
        paletteName.textContent = palette.name;

        const colorDiv = document.createElement("div");
        colorDiv.classList.add("colors");

        // loop through each color in the palette
        palette.colors.forEach((color) => {
            const colorBox = document.createElement("div");
            colorBox.classList.add("color");
            colorBox.style.backgroundColor = color;
            colorDiv.appendChild(colorBox);

            // create a <p> tag and add a class to it
            const colorName = document.createElement("p");
            colorName.classList.add("color-name");
            colorName.innerText = color;
            colorBox.appendChild(colorName);
        });
        paletteDiv.appendChild(paletteName);
        paletteDiv.appendChild(colorDiv);
        familyDiv.appendChild(paletteDiv);

    });
    familyContainer.appendChild(familyDiv);

});